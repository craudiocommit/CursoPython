"""
Operadores aritiméticos
+, -, *, /div com result. float, //div com result. inteiro, **pontenciação, %retorna o resto da div, ()presedência
"""
print('Multiplicação *', 10 * 10)
print('Adição +', 10 + 10)
print('Subtração -', 10 - 5)
print('Divisão float /', 10 / 2)
print('Divisão float /', 10 / 3)
print('Divisão int', 10 // 3)
print('Potenciação **', 2 ** 10)
print('Resto da divisão %', 10 % 5)
print('Resto da divisão %', 10 % 3)
print('Presedência ()', 5+2*10)
print('Presedência ()', ((5+2)*10))